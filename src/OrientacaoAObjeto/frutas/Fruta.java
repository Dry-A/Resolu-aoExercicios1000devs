package OrientacaoAObjeto.frutas;

public class Fruta {

    public String nome;
    public Integer idFruta;
    public int quantidade;
    public float preco;

    public Fruta(String nome, Integer idFruta, int quantidade, float preco) {
        this.nome = nome;
        this.idFruta = idFruta;
        this.quantidade = quantidade;
        this.preco = preco;
    }
}
